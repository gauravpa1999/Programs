package Practiceset;
//Widening of Short
public class WShort {
	public static void main(String[] args) {
		short s = 3423;
		int i = s;
		long l = i;
		float f = l;
		double d = f;
		
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		
	}

}
